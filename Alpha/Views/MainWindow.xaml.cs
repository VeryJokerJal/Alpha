        private void textEditor_TextArea_TextEntered(object sender, TextCompositionEventArgs e)
        {
            if (string.IsNullOrWhiteSpace(e.Text))
            {
                return;
            }
            if (e.Text == "(")
            {
                textEditor.TextArea.Document.Insert(textEditor.CaretOffset, ")");
                textEditor.TextArea.Caret.Offset = textEditor.CaretOffset - 1;
            }

            TriggerAutoComplete(textEditor.TextArea);
        }

        private void textEditor_TextArea_TextEntering(object sender, TextCompositionEventArgs e)
        {
            if (!string.IsNullOrWhiteSpace(e.Text) && completionWindow != null && e.Text == "(")
            {
                completionWindow.CompletionList.RequestInsertion(e);
            }
        }

        private void SetCustomSyntaxHighlighting()
        {
            textEditor.SyntaxHighlighting = new HighlightingDefinition();
        }

        public void TriggerAutoComplete(TextArea textArea)
        {
            // 获取当前输入的内容（光标前的文本）
            string currentWord = textArea.Document.GetText(textArea.Selection.StartPosition.Column, textArea.Caret.Offset - textArea.Selection.EndPosition.Column);

            string input = currentWord.Split(' ', '+', '-', '*', '/', '=', ',', '(', ')', '\n', '<', '>').Last();
            // 添加符合当前输入的补全项
            IEnumerable<CompletionData> suggestions = GetMatchingSuggestions(input);

            if (!suggestions.Any())
            {
                return;
            }
            // 创建一个 CompletionWindow 实例，绑定到 TextArea
            completionWindow = new(textArea)
            {
                Width = 450,
                WindowStyle = WindowStyle.None,
                ResizeMode = ResizeMode.NoResize,
                Background = new SolidColorBrush(Color.FromRgb(28, 28, 35)),  // 深邃的午夜蓝黑色
                Foreground = new SolidColorBrush(Color.FromRgb(238, 237, 240)),  // 珍珠般的暖白色
            };
            CompletionList data = completionWindow.CompletionList;

            // 将符合条件的补全项添加到列表
            for (int i = 0; i < suggestions.Count(); i++)
            {
                if (i > 30)
                {
                    break;
                }

                CompletionData item = suggestions.ElementAt(i);
                item.SplitText = input;
                data.CompletionData.Add(item);
            }
            data.SelectedItem = suggestions.First();

            // 设置补全窗口的位置
            completionWindow.Show();

            // 监听 CompletionWindow 关闭事件，处理补全项的插入
            completionWindow.Closed += (sender, e) =>
            {
                completionWindow = null;
            };
        }

        // 根据当前输入的单词，进行模糊匹配（可以扩展为正则匹配或更复杂的规则）
        private IEnumerable<CompletionData> GetMatchingSuggestions(string currentWord)
        {
            // 如果没有输入，则不返回任何建议
            if (string.IsNullOrWhiteSpace(currentWord))
            {
                return [];
            }

            // 初始化补全项列表
            List<CompletionData> completionSuggestions = [];

            if (Settings.User.BackTestModeIndex == 2)
            {
                for (int i = 0; i < 14; i++)
                {
                    completionSuggestions.Add(new CompletionData($"${i + 1}", CompletionType.Placeholder));
                }
            }

            for (int i = 0; i < completionKeywordSuggestions.Count; i++)
            {
                completionSuggestions.Add(new CompletionData(completionKeywordSuggestions[i], CompletionType.Other));
            }
            for (int i = 0; i < completionVariableSuggestions.Count; i++)
            {
                completionSuggestions.Add(new CompletionData(completionVariableSuggestions[i], CompletionType.Variable));
            }
            for (int i = 0; i < completionFunctionSuggestions.Count; i++)
            {
                completionSuggestions.Add(new CompletionData(completionFunctionSuggestions[i], CompletionType.Function));
            }


            // 使用简单的大小写不敏感的包含匹配，返回匹配的补全项
            return completionSuggestions
                .Where(suggestion => suggestion.Text?.Contains(currentWord, StringComparison.InvariantCultureIgnoreCase) == true)
                .OrderBy(suggestion => suggestion.Text?.IndexOf(currentWord, StringComparison.InvariantCultureIgnoreCase) ?? -1); // 根据匹配的顺序排序
        }

