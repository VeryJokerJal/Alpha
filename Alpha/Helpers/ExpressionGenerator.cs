namespace Alpha.Helpers
{
    public class ExpressionGenerator
    {
        [Flags]
        public enum FieldType
        {
            Matrix = 0,
            Vector = 1,
            Universe = 2,
            Group = 4,
            Boolean = 8,
        }

        [Flags]
        public enum FunctionType
        {
            Arithmetic = 0,
            Matrix = 1,
            Vector = 2,
            TimeSeries = 4,
            CrossSectional = 8,
            Transformational = 16,
            Group = 32,
            Logical = 64
        }

        [Flags]
        public enum UserDefinedType
        {
            User = 0,
            Consultant = 1
        }

        // 字段类，用于手动指定字段名和类型
        public class Field
        {
            public string Name { get; set; }
            public FieldType Type { get; set; }
            public UserDefinedType UserType { get; set; }

            public Field(string name, FieldType type, UserDefinedType userType)
            {
                Name = name;
                Type = type;
                UserType = userType;
            }
        }

        // 定义运算符
        public class Operator
        {
            public string Symbol { get; }
            public Func<object, object, object> Operation { get; }
            public FieldType ResultType { get; }
            public List<FieldType> OperandTypes { get; }

            public Operator(string symbol, Func<object, object, object> operation, FieldType resultType, List<FieldType> operandTypes)
            {
                Symbol = symbol;
                Operation = operation;
                ResultType = resultType;
                OperandTypes = operandTypes;
            }

            public bool CanApply(FieldType type1, FieldType type2)
            {
                return OperandTypes.Contains(type1) && OperandTypes.Contains(type2);
            }
        }
        public class FunctionParameter
        {
            public string Name { get; }
            public FieldType Type { get; }
            public object? DefaultValue { get; }

            public FunctionParameter(string name, FieldType type, object? defaultValue = null)
            {
                Name = name;
                Type = type;
                DefaultValue = defaultValue;
            }
        }

        public class Function
        {
            public string Name { get; }
            public FunctionType Type { get; }
            public UserDefinedType UserType { get; }
            public List<FunctionParameter> Parameters { get; }

            public Function(string name, FunctionType type, UserDefinedType userType, List<FunctionParameter> parameterTypes)
            {
                Name = name;
                Type = type;
                UserType = userType;
                Parameters = parameterTypes;
            }
        }

